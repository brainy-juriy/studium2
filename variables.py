# описание еды

# str
# название
name = 'мясное рагу'
print(type(name))

# вид - белковая пища
species = 'белковый'
print(type(species))

# группа - животного происхождения
group = 'животная'
print(type(group))


# int
# вес в граммах
weight = 500
print(type(weight))


# float
# калорийность в ккал
caloric = 875.6
print(type(caloric))


# bool
# вкусно или невкусно
is_delicious = True
print(type(is_delicious))















